DEFAULT_CHARACTER_ID = "haru"


def get_character(character_id: str) -> dict:
    """캐릭터 정보 반환 (없으면 기본값 하루)"""
    return CHARACTERS.get(character_id, CHARACTERS[DEFAULT_CHARACTER_ID])


def get_all_characters() -> list[dict]:
    """시스템 프롬프트 제외한 캐릭터 목록 반환"""
    return [
        {k: v for k, v in c.items() if k != "system_prompt"}
        for c in CHARACTERS.values()
    ]
